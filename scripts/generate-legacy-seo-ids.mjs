/**
 * Genera lib/legacy-seo-id-redirects.generated.ts desde el API (id_lugar → /pueblos/slug).
 * Uso: node scripts/generate-legacy-seo-ids.mjs
 * API: NEXT_PUBLIC_API_URL o por defecto producción Railway.
 */
const API_BASE =
  (process.env.NEXT_PUBLIC_API_URL || 'https://lpmbe-backend-production.up.railway.app').replace(
    /\/$/,
    ''
  );
const fs = require('fs');
const path = require('path');

async function main() {
  const res = await fetch(`${API_BASE}/pueblos`);
  if (!res.ok) {
    console.error('Error fetching pueblos:', res.status);
    process.exit(1);
  }
  const pueblos = await res.json();
  const ficha = {};
  const semaforo = {};
  for (const p of pueblos) {
    const id = String(p.id);
    const dest = `/pueblos/${p.slug}`;
    ficha[id] = dest;
    semaforo[id] = dest;
  }
  const ids = Object.keys(ficha)
    .map(Number)
    .sort((a, b) => a - b);
  const lines = [
    '/** Generated by scripts/generate-legacy-seo-ids.mjs - do not edit by hand */',
    'export const LEGACY_FICHA_ID_REDIRECTS: Record<string, string> = {',
    ...ids.map((id) => `  '${id}': '${ficha[String(id)]}',`),
    '};',
    'export const LEGACY_SEMAFORO_ID_REDIRECTS: Record<string, string> = {',
    ...ids.map((id) => `  '${id}': '${semaforo[String(id)]}',`),
    '};',
  ];
  const outPath = path.join(__dirname, '..', 'lib', 'legacy-seo-id-redirects.generated.ts');
  fs.writeFileSync(outPath, lines.join('\n'));
  console.log('Written', ids.length, 'ids to lib/legacy-seo-id-redirects.generated.ts');
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
